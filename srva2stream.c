bool set_nonblock(SOCKET s)
{
  u_long arg = 1;
  if (ioctlsocket(s, FIONBIO, &arg) == SOCKET_ERROR)
  {
    fprintf(stderr, "ioctlsocket(): %d\n", WSAGetLastError());
    return true;
  }
  return false;
}

int open_pipe(int port)
{
  char pipe_name[0x100];
  sprintf(pipe_name, "\\\\.\\pipe\\a2stream-%d", port);

  HANDLE pipe = CreateNamedPipe(pipe_name, PIPE_ACCESS_INBOUND, PIPE_NOWAIT, 1, 512, 512, 0, NULL);
  if (pipe == INVALID_HANDLE_VALUE)
  {
    fprintf(stderr, "cannot create %s\n", pipe_name);
    return -1;
  }

  fprintf(stderr, "waiting on pipe %s\n", pipe_name);
  while (ConnectNamedPipe(pipe, NULL) == false)
  {
    DWORD err = GetLastError();
    if (err == ERROR_PIPE_LISTENING)
    {
      continue;
    }
    if (err == ERROR_PIPE_CONNECTED)
    {
      break;
    }
    fprintf(stderr, "cannot connect %s\n", pipe_name);
    return -1;
  }
  fprintf(stderr, "connect on pipe\n");

  int file = _open_osfhandle((intptr_t)pipe, O_RDONLY | O_BINARY);
  if (file == -1)
  {
    perror("_open_osfhandle()");
    return -1;
  }

  return file;
}

