bool set_nonblock(int f)
{
  if (fcntl(f, F_SETFL, fcntl(f, F_GETFL, 0) | O_NONBLOCK) == -1)
  {
    fprintf(stderr, "fcntl(): %d\n", errno);
    return true;
  }
  return false;
}

int open_pipe(int port)
{
  char pipe_name[0x100];
  sprintf(pipe_name, "/tmp/a2stream-%d", port);

  unlink(pipe_name);
  if (mkfifo(pipe_name, 0666) == -1)
  {
    perror(pipe_name);
    return -1;
  }

  fprintf(stderr, "waiting on %s\n", pipe_name);
  int pipe = open(pipe_name, O_RDONLY);
  if (pipe != -1)
  {
    fprintf(stderr, "connect on pipe\n");
  }
  else
  {
    perror(pipe_name);
  }

  if (pipe != -1 && set_nonblock(pipe))
  {
    pipe == -1;
  }

  unlink(pipe_name);
  return pipe;
}

