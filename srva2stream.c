bool send_http_header(SOCKET s)
{
  static const char http_header[] = "HTTP/1.0 200 OK\r\n\r\n";

  if (send(s, http_header, sizeof(http_header) - 1, 0) != sizeof(http_header) - 1)
  {
    fprintf(stderr, "send_http_header(): %d\n", WSAGetLastError());
    return true;
  }
  return false;
}

bool send_type(SOCKET s)
{
  static const uint8_t type[2] = {STREAM_TYPE_MAJOR, STREAM_TYPE_MINOR};

  if (send(s, type, sizeof(type), 0) != sizeof(type))
  {
    fprintf(stderr, "send_type(): %d\n", WSAGetLastError());
    return true;
  }
  return false;
}

extern uint8_t dhgr[0x4000];

bool load_cover(int port)
{
  char cover_name[0x100];
  sprintf(cover_name, "srva2stream-%d.dhgr", port);
  
  int cover = open(cover_name, O_RDONLY | O_BINARY);
  if (cover == -1)
  {
    fprintf(stderr, "%s not found - using default\n", cover_name);
    return false;
  }
  if (read(cover, dhgr, sizeof(dhgr)) != sizeof(dhgr))
  {
    perror(cover_name);
    return true;
  }
  close(cover);

  static const int lines[12] = {0x0BD0, 0x0FD0, 0x13D0, 0x17D0, 0x1BD0, 0x1FD0,
                                0x2BD0, 0x2FD0, 0x33D0, 0x37D0, 0x3BD0, 0x3FD0};

  for (int i = 0; i < sizeof(lines) / sizeof(lines[0]); i++)
  {
    memset(dhgr + lines[i], PIXEL_BLACK, 40);
  }

  return false;
}

bool send_cover(SOCKET s)
{
  if (send(s, dhgr, sizeof(dhgr), 0) != sizeof(dhgr))
  {
    fprintf(stderr, "send_cover(): %d\n", WSAGetLastError());
    return true;
  }
  return false;
}

void set_pixel(uint8_t *line, int pos, int color)
{
  uint32_t value = line[pos / 7 * 2 + 40 + 1] << 24 |
                   line[pos / 7 * 2 +      1] << 16 |
                   line[pos / 7 * 2 + 40    ] <<  8 |
                   line[pos / 7 * 2         ];

  // The DHGR display encodes 7 pixels across interleaved
  // 4-byte sequences of AUX and MAIN memory, as follows:
  // 0BBBAAAA  0DDCCCCB  0FEEEEDD  0GGGGFFF
  // Aux N     Main N    Aux N+1   Main N+1  (N even)
  static const int shift[7][4] = {{ 0,  0,  0,  0},  // A
                                  { 4,  4,  4,  5},  // B
                                  { 9,  9,  9,  9},  // C
                                  {13, 13, 14, 14},  // D
                                  {18, 18, 18, 18},  // E
                                  {22, 23, 23, 23},  // F
                                  {27, 27, 27, 27}}; // G

  for (int bit = 0; bit < 4; bit++)
  {
    if (color & (1 << bit))
    {
      value |= (1 << bit) << shift[pos % 7][bit];
    }
    else
    {
      value &= ~((1 << bit) << shift[pos % 7][bit]);
    }
  }

  line[pos / 7 * 2 + 40 + 1] = value >> 24;
  line[pos / 7 * 2 +      1] = value >> 16;
  line[pos / 7 * 2 + 40    ] = value >>  8;
  line[pos / 7 * 2         ] = value;
}

bool send_visual_templates(SOCKET s)
{
  uint8_t line[80];
  memset(line, 0, 80);
  line[0] = 1;

  #define LEN (PIXEL_MID_POS - PIXEL_MIN_POS)

  for (int pos = 0; pos < VISUAL_NUM_VAL / 2; pos++)
  {
    if (pos <= LEN)
    {
      int color = PIXEL_GREEN;
      if (pos > LEN - 30)
      {
        color = PIXEL_YELLOW;
      }
      if (pos > LEN - 15)
      {
        color = PIXEL_ORANGE;
      }
      set_pixel(line + 1, PIXEL_MID_POS - pos,     color);
      set_pixel(line + 1, PIXEL_MID_POS + pos + 1, color);
    }
    if (send(s, line, sizeof(line), 0) != sizeof(line))
    {
      fprintf(stderr, "send_visual_templates(): %d\n", WSAGetLastError());
      return true;
    }
  }

  #undef LEN

  return false;
}

uint8_t sample_base[SAMPLE_CHUNK_SIZE];
uint8_t visual_base[VISUAL_NUM_VAL];

void init_tables(void)
{
  for (int i = 0; i < SAMPLE_CHUNK_SIZE; i++)
  {
    sample_base[i] = i <  SAMPLE_LO_2_HI ||
                     i >= SAMPLE_HI_2_LO ? SAMPLE_LO_BASE
                                         : SAMPLE_HI_BASE;
  }

  for (int i = 0; i < VISUAL_NUM_VAL; i++)
  {
    visual_base[i] = i < VISUAL_LO_2_HI ? VISUAL_LO_BASE + i
                                        : VISUAL_HI_BASE + i - VISUAL_LO_2_HI;
  }
}

void convert_chunk(const float *x, uint8_t *y)
{
  static float sample_max = 0.0;
  static int visual_val = -1;
  static int visual_level = 0;

  for (int i = 0; i < SAMPLE_CHUNK_SIZE; i++)
  {
    float val = fabsf(x[i]);
    if (val > sample_max && val < 1.0)
    {
      sample_max = val;
    }
  }

  int sample = 0;
  for (int i = 0; i < OUTPUT_CHUNK_SIZE; i++)
  {
    if (i == VISUAL_CHUNK_INDX)
    {
      if (visual_val == -1)
      {
        int val = (int)(sample_max * (PIXEL_MID_POS - PIXEL_MIN_POS) * 2.0);
        if (val < visual_level)
        {
          visual_level -= 2;
          if (visual_level < 0)
          {
            visual_level = 0;
          }
        }
        else
        {
          visual_level += (val - visual_level) / 5;
        }
        visual_val = visual_level;
        y[i] = visual_base[visual_val & ~1];
      }
      else
      {
        y[i] = visual_base[visual_val | 1];
        sample_max = 0.0;
        visual_val = -1;
      }
      continue;
    }

    int32_t sample_val = (int32_t)((x[sample] + 1.0) / 2.0 * SAMPLE_MAX_VAL);

    if (sample_val < 0)
    {
      sample_val = 0;
    }
    else if (sample_val > SAMPLE_MAX_VAL)
    {
      sample_val = SAMPLE_MAX_VAL;
    }

    y[i] = sample_base[sample++] + (uint8_t)sample_val;
  }
}

float samples_buf[0x40][SAMPLE_CHUNK_SIZE];
int   samples_len = 0;

uint8_t output_buf[0x100 * OUTPUT_CHUNK_SIZE];
uint8_t *output_rd;
uint8_t *output_wr;
bool   output_full;

void init_output(void)
{
  output_rd = output_buf;
  output_wr = output_buf;
  output_full = false;
}

bool send_output(SOCKET s, const uint8_t *buf, int len)
{
  int retval = send(s, buf, len, 0);
  if (retval == SOCKET_ERROR)
  {
    int error = WSAGetLastError();
    if (error != WSAEWOULDBLOCK)
    {
      fprintf(stderr, "send(): %d\n", error);
      return true;
    }
  }
  else
  {
    output_rd += retval;
    if (output_rd == output_buf + sizeof(output_buf))
    {
      output_rd = output_buf;
    }
    output_full = false;
  }
  return false;
}

SOCKET open_server(int port)
{
#ifdef _WIN32
  WSADATA wsa;
  if (WSAStartup(MAKEWORD(2, 2), &wsa) != 0)
  {
    fprintf(stderr, "WSAStartup(): Failure\n");
    return INVALID_SOCKET;
  }
#endif

  SOCKET srv = socket(AF_INET, SOCK_STREAM, IPPROTO_TCP);
  if (srv == INVALID_SOCKET)
  {
    fprintf(stderr, "socket(): %d\n", WSAGetLastError());
    return INVALID_SOCKET;
  }

  if (set_nonblock(srv))
  {
    return INVALID_SOCKET;
  }

  int opt = 1;
  if (setsockopt(srv, SOL_SOCKET, SO_REUSEPORT, (const char *)&opt, sizeof(opt)) == SOCKET_ERROR)
  {
    fprintf(stderr, "setsockopt(): %d\n", WSAGetLastError());
    return INVALID_SOCKET;
  }

  SOCKADDR_IN local;
  local.sin_family      = AF_INET;
  local.sin_addr.s_addr = INADDR_ANY;
  local.sin_port        = htons(port);
  if (bind(srv, (SOCKADDR *)&local, sizeof(local)) == SOCKET_ERROR)
  {
    fprintf(stderr, "bind(): %d\n", WSAGetLastError());
    return INVALID_SOCKET;
  }

  if (listen(srv, SOMAXCONN) == SOCKET_ERROR)
  {
    fprintf(stderr, "listen(): %d\n", WSAGetLastError());
    return INVALID_SOCKET;
  }

  return srv;
}

int main(int argc, const char *argv[])
{
  static_assert(sizeof(float) == 4, "float isn't 32-bit");

  if (argc != 2)
  {
    fprintf(stderr,
            "usage: %s port\n"
            "       port: TCP port number to listen on\n",
            argv[0]);
    return EXIT_FAILURE;
  }

  int port = atoi(argv[1]);
  if (port < 0x0001 || port > 0xFFFF)
  {
    fprintf(stderr, "%s: invalid port number\n", argv[1]);
    return EXIT_FAILURE;
  }

  if (load_cover(port))
  {
    return EXIT_FAILURE;
  }

  init_tables();

  int pipe = open_pipe(port);
  if (pipe == -1)
  {
    return EXIT_FAILURE;
  }

  SOCKET srv = open_server(port);
  if (srv == INVALID_SOCKET)
  {
    return EXIT_FAILURE;
  }

  SOCKET con = INVALID_SOCKET;

  while (true)
  {
    int retval = read(pipe, (char *)(samples_buf) + samples_len, sizeof(samples_buf) - samples_len);
    if (retval == -1)
    {
      if (_doserrno != ERROR_NO_DATA)
      {
        perror("read()");
        return EXIT_FAILURE;
      }
    }
    else
    {
      if (retval == 0)
      {
        fprintf(stderr, "disconnect on pipe\n");
        return EXIT_SUCCESS;
      }
      else
      {
        samples_len += retval;
      }
    }

    if (samples_len == sizeof(samples_buf))
    {
      samples_len = 0;

      if (con != INVALID_SOCKET)
      {
        for (int chunk = 0; chunk < sizeof(samples_buf) / sizeof(samples_buf[0]); chunk++)
        {
          if (output_wr < output_rd && output_wr + OUTPUT_CHUNK_SIZE > output_rd || output_full)
          {
            static unsigned long counter = 1;
            fprintf(stderr, "overflow (%luth time)\n", counter++);
            break;
          }

          convert_chunk(samples_buf[chunk], output_wr);

          output_wr += OUTPUT_CHUNK_SIZE;
          if (output_wr == output_buf + sizeof(output_buf))
          {
            output_wr = output_buf;
          }
          if (output_wr == output_rd)
          {
            output_full = true;
          }
        }
      }
    }

    if (con == INVALID_SOCKET)
    {
      // connect?

      SOCKADDR_IN remote;
      unsigned remote_size = sizeof(remote);
      con = accept(srv, (SOCKADDR *)&remote, &remote_size);
      if (con == INVALID_SOCKET)
      {
        int error = WSAGetLastError();
        if (error != WSAEWOULDBLOCK)
        {
          fprintf(stderr, "accept(): %d\n", error);
          return EXIT_FAILURE;
        }
      }
      else
      {
        if (send_http_header(con))
        {
          return EXIT_FAILURE;
        }

        if (send_type(con))
        {
          return EXIT_FAILURE;
        }

        if (send_cover(con))
        {
          return EXIT_FAILURE;
        }

        if (send_visual_templates(con))
        {
          return EXIT_FAILURE;
        }

        if (set_nonblock(con))
        {
          return EXIT_FAILURE;
        }

        init_output();

        fprintf(stderr, "connect from A2Stream at %s\n", inet_ntoa(remote.sin_addr));
      }
    }
    else
    {
      // disconnect?

      char dummy[0x100];
      int retval = recv(con, dummy, sizeof(dummy), 0);
      if (retval == SOCKET_ERROR)
      {
        int error = WSAGetLastError();
        if (error != WSAEWOULDBLOCK)
        {
          if (error != WSAECONNABORTED &&
              error != WSAECONNRESET)
          {
            fprintf(stderr, "recv(): %d\n", error);
            return EXIT_FAILURE;
          }
          retval = 0;
        }
      }
      if (retval == 0)
      {
        closesocket(con);
        con = INVALID_SOCKET;

        fprintf(stderr, "disconnect from A2Stream\n");
      }
    }

    if (con != INVALID_SOCKET)
    {
      if (output_rd > output_wr || output_full)
      {
        if (send_output(con, output_rd, output_buf + sizeof(output_buf) - output_rd))
        {
          return EXIT_FAILURE;
        }
      }
            
      if (output_rd < output_wr)
      {
        if (send_output(con, output_rd, output_wr - output_rd))
        {
          return EXIT_FAILURE;
        }
      }
    }

    Sleep(10);
  }
  return EXIT_SUCCESS;
}

